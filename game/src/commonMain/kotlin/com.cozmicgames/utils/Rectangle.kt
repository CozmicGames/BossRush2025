package com.cozmicgames.utils

class Rectangle() {
    constructor(x: Float, y: Float, width: Float, height: Float) : this() {
        this.x = x
        this.y = y
        this.width = width
        this.height = height
    }

    var x = 0.0f
    var y = 0.0f
    var width = 0.0f
    var height = 0.0f
}